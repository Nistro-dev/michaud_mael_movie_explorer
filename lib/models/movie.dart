class Movie {
  final String title;
  final String year;
  final String poster;
  final String plot;
  final String director;
  final String actors;
  final String genre;
  final String rating;

  Movie({
    required this.title,
    required this.year,
    required this.poster,
    required this.plot,
    required this.director,
    required this.actors,
    required this.genre,
    required this.rating,
  });
}
